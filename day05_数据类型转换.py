# 数字类型转换为字符串
num_str=str(10)
print(type(num_str),num_str)

float_str=str(3.14)
print(type(float_str),float_str)
# 字符串类型转换为数字
num=int("10")
print(type(num),num)

num2=float("3.14")
print(type(num2),num2)
# 整数类型转换为浮点数
num3=float(10)
print(type(num3),num3)

# 浮点数类型转换为整数
num4=int(3.14)
print(type(num4),num4)